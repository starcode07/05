    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        paquete.setRemitente(jTextField1.getText());
        paquete.setReceptor(jTextField2.getText());
        paquete.setPeso(Float.valueOf(jTextField3.getText()));
        paquete.setDescripcion(jTextPane1.getText());
        paquete.setDestino(jTextField4.getText());
        paquete.setFragil(jCheckBox_tipo.isSelected());
        

        
        try {
             if ("DHL".equals(jComboBox1.getSelectedItem().toString())) {
                 selectEmpresa = new DhlExpress(paquete);
                 totalImporte=(float) 2.1;

            }
             if ("Mundo Express".equals(jComboBox1.getSelectedItem().toString())) {
                selectEmpresa = new MundoExpress(paquete);
                totalImporte=(float) -0.1;
            }
             if ("Envio Express".equals(jComboBox1.getSelectedItem().toString())) {
                 selectEmpresa = new EnvioExpress(paquete);
                 totalImporte=(float) 3.1;

            }
            
        } catch (Exception e) {
            selectEmpresa = new DhlExpress();
        }
        
        
        try {
             if ("Avion".equals(jComboBox2.getSelectedItem().toString())) {
                 selectModo=new EnvioAire();
 
            }
              if ("Barco".equals(jComboBox2.getSelectedItem().toString())) {
                 selectModo=new Terrestre();
 
            }
            
        } catch (Exception e) {
            selectModo=new EnvioAire();
        }
        

        
        selectEmpresa.setEnvio(selectModo);
        totalImporte=selectEmpresa.precioEmpresa(totalImporte);
        
             

        
        jEditorPane1.setText("Remitente: "+selectEmpresa.getPaquete().getRemitente()+"\n"
                             +"Receptor: "+selectEmpresa.getPaquete().getReceptor()+"\n"
                             +"Destino: "+selectEmpresa.getPaquete().getDestino()+"\n"
                             +"Peso (kg): "+selectEmpresa.getPaquete().getPeso()+"\n"
                             +"Descripcion: "+selectEmpresa.getPaquete().getDescripcion()+"\n"
                             +"Fragil: "+selectEmpresa.getPaquete().isFragil()+"\n"
                             +"Empresa: "+jComboBox1.getSelectedItem().toString()+"\n"
                             +"Modo de envio: "+jComboBox2.getSelectedItem().toString()+"\n"
                             +"Importe Total (Bs): "+totalImporte*selectEmpresa.getPaquete().getPeso()
        );
    }//GEN-LAST:event_jButton1ActionPerformed

